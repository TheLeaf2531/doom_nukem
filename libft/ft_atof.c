#include "libft.h"

float	ft_atof(const char *s)
{
	float r;
	float f;
	float d;
	int p;

	r = 0.0f;
	f = 1.0F;
	if (*s == '-')
	{
		f = -1.0F;
		s++;
	}
	while (*s)
	{
		p = *s == '.' ? 1 : 0;
		d = *s - '0';
		if (d >= 0.0F && d <= 9.0F)
		{
			f = p == 1 ? f / 10.0f : f;
			r = r * 10.0f + d;
		}
		s++;
	}
	return	(r * f);
}