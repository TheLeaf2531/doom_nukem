#include "../include/doom_nukem.h"

int			elem_nbrs(char const *s, char c)
{
	int	n;

	n = 0;
	while (*s)
	{
		if (*s != c && (*(s + 1) == c || *(s + 1) == '\0'))
			n++;
		s++;
	}
	return (n);
}